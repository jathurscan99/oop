    private void eqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eqlActionPerformed
        num2=Double.parseDouble(disply.getText());
        if (act =="+")
        {
            result=num1+num2;
            disply.setText(String.valueOf(result));
        }
        else if (act =="-")
        {
            result=num1-num2;
            disply.setText(String.valueOf(result));
        }
        else if (act =="*")
        {
            result=num1*num2;
            disply.setText(String.valueOf(result));
        }
        else if (act =="/")
        {
            result=num1/num2;
            disply.setText(String.valueOf(result));
        }
                
    }//GEN-LAST:event_eqlActionPerformed

